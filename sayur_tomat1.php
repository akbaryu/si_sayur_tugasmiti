                                    <p>Kutu daun hijau (Aphis sp.) merupakan vektor pembawa virus. Jadi, tanaman tomat yang dihinggapi kutu ini kan terkena penyebaran virus. Ukuran panjang kutu hijau sekitar 2 mm. Ada yang bersayap dan tidak bersayap. Kutu yang bersayap warna kepala dan dadanya coklat hingga kehitaman, bagian perutnya biasanya berwarna hijau kekuningan. Kutu yang tidak bersayap berwarna hijau kekuningan.
Daun yang terserang kutu hijau bervariasi, daun menjadi keriting dan kerdil, bentuknya melengkung ke bawah. Bisa juga daun menyempit seperti pita. Warna daun mozaik dan daun menjadi rapuh.
Untuk menekan perkembangan hama ini bisa dengan penggunaan mulsa plastik perak, kutu hijau tidak menyukai pantulan sinar matahari. Penyemprotan bisa memanfaatkan insektisida..<br>
<br>
<hr>
ROTRAZ 200 EC mengandung bahan aktif amitraz 200 g/l yang merupakan racun kontak dan racun pernafasan yang kuat, sehingga tungau dan serangga hama sasaran yang terkena maupun yang tidak terkena semprotan akan mati, dan tanaman terbebas sama sekali dari serangan hama tersebut.
<br>
<br>
- Hama Sasaran : Kutu daun
<br>
- Konsentrasi Formulasi : 1 - 2 ml
<br>
- Cara dan Waktu Aplikasi :<br>
